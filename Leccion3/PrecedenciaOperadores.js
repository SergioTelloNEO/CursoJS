//Operadores de asignacion

let a = 1;

//Asignacion compuesto
a += 3; // a = a + 3 
console.log(a);

//asignacion de resta
a -= 2; // a = a - 2
console.log(a);

//asignacion de multiplicacion 
a *= 2; // a = a * 2
console.log(a);

//asignacion de division
a /= 2; // a = a / 2;
console.log(a);

//asignacion de modulo (residuo de la division)
a %= 2; // a = a % 2;
console.log(a);

let b = 3;

//asignacion de exponente
b **=  2; // a = a ** 2;
console.log(b);