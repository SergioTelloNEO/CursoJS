let a = 3, b= 2;


z = a + b;
console.log( "Resultado de la suma: " + z );

z = a - b;
console.log( "Resultado de la resta es: " + z );

z = a * b;
console.log("Resultado de la multiplicacion es: " + z);

z = a / b;
console.log("Resultado de la division es: " + z);

z = a % b; //Residuo de la division
console.log("Resultado de operacion modulo (residuo) es: " + z);

z = a ** b;
console.log("Resultado de el exponente es: " + z);

//Incremento
//Pre-incremento(el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);


//Post-incremento(el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento
//Predecremento

z = --a;
console.log(a);
console.log(z);

//postdecremento
z = b--;
console.log(b);
console.log(z);